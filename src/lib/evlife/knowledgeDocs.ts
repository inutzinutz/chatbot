export function matchKnowledgeDoc(message: string): KnowledgeDoc | undefined {
  const lower = message.toLowerCase();
  return knowledgeDocs.find((d) =>
    d.triggers.some((t) => lower.includes(t.toLowerCase()))
  );
}
