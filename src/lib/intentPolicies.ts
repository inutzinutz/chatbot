export function matchIntent(message: string): Intent | undefined {
  const lower = message.toLowerCase();
  return intents.find(
    (intent) =>
      intent.active &&
      intent.triggers.length > 0 &&
      intent.triggers.some((t) => lower.includes(t.toLowerCase()))
  );
}
